//! ARM64 线程上下文管理
//!
//! 本模块实现线程上下文的保存和恢复，是内核调度器的核心组件。
//! 上下文切换时，必须保存/恢复 callee-saved 寄存器（x19-x30）和栈指针。

use core::arch::asm;

/// 线程上下文结构体
///
/// 存储线程执行时的关键状态，用于上下文切换时保存和恢复线程状态。
///
/// # 为什么使用 #[repr(C)]
///
/// **必须使用 C 语言布局**，原因如下：
///
/// 1. **汇编代码访问**：上下文切换的汇编代码按固定偏移访问结构体字段
///    ```ignore
///    str x0, [{from}]   // {from} = ThreadContext 指针，按偏移 0 存储 SP
///    ldr x0, [{to}]     // {to} = ThreadContext 指针，按偏移 0 加载 SP
///    ```
///
/// 2. **布局固定性**：Rust 默认可能重排字段，但汇编代码硬编码了偏移量
///
/// 3. **跨模块兼容**：可能与 C 语言调度器交互，需要布局一致
///
/// # 字段说明
///
/// 当前只保存栈指针 (SP)，完整实现应包含：
/// - 所有 callee-saved 寄存器（x19-x30）
/// - 栈指针 (SP)
/// - 程序计数器 (PC)（可选，通常用 LR）
///
/// # 内存布局
///
/// ```text
/// +----------+
/// |   sp     |  偏移 0，8 字节
/// +----------+
/// ```
#[repr(C)]
pub struct ThreadContext {
    /// 栈指针 - 栈顶位置
    ///
    /// SP 指向线程栈的当前位置，保存了：
    /// - 局部变量
    /// - 函数调用链（栈帧）
    /// - 返回地址（LR）
    ///
    /// 上下文切换时，保存当前 SP 到 `from`，从 `to` 加载新 SP。
    sp: u64,
}

/// 硬件级上下文切换函数
///
/// 实现线程之间的上下文切换，是调度器的核心操作。
///
/// # 参数
///
/// - `_from`: 当前线程的上下文指针（保存当前状态）
/// - `_to`: 目标线程的上下文指针（恢复目标状态）
///
/// # 执行流程
///
/// ```text
/// 1. 保存当前线程状态：
///    - 压栈保存 callee-saved 寄存器（x19-x30）
///    - 保存当前栈指针 SP 到 _from->sp
///
/// 2. 切换到目标线程：
///    - 从 _to->sp 加载目标线程的栈指针
///    - 更新当前 SP
///
/// 3. 恢复目标线程状态：
///    - 出栈恢复 callee-saved 寄存器（x19-x30）
///    - 返回后继续执行目标线程
/// ```
///
/// # 寄存器操作详解
///
/// ## 保存阶段（压栈）
///
/// ```ignore
/// stp x29, x30, [sp, #-16]!  // 保存 FP + LR
/// stp x27, x28, [sp, #-16]!  // 保存 x27-x28
/// stp x25, x26, [sp, #-16]!  // ...
/// stp x23, x24, [sp, #-16]!
/// stp x21, x22, [sp, #-16]!
/// stp x19, x20, [sp, #-16]!  // 共保存 6 对寄存器（12 个）
/// ```
///
/// - `stp x29, x30, [sp, #-16]!`: Store Pair，同时保存两个寄存器到栈
/// - `!` 表示预递减 SP（SP = SP - 16，然后存储）
/// - 保存顺序：从 x30 到 x19（倒序）
///
/// ## 保存栈指针
///
/// ```ignore
/// mov x0, sp              // x0 = 当前栈指针
/// str x0, [x{from}]       // 将 SP 存储到 from->sp
/// ```
///
/// - `[{from}]` 表示访问 from 指针指向的内存地址
/// - 由于 ThreadContext 只有一个字段 sp，偏移为 0
///
/// ## 加载目标栈指针
///
/// ```ignore
/// ldr x0, [{to}]          // 从 to->sp 加载目标线程的 SP
/// mov sp, x0              // 更新当前栈指针为目标线程的栈
/// ```
///
/// - 此时栈已切换，SP 指向目标线程的栈
/// - 目标线程栈中已保存了它的寄存器状态
///
/// ## 恢复阶段（出栈）
///
/// ```ignore
/// ldp x19, x20, [sp], #16  // 恢复 x19-x20
/// ldp x21, x22, [sp], #16  // 恢复 x21-x22
/// // ... 恢复所有寄存器
/// ldp x29, x30, [sp], #16  // 恢复 FP + LR
/// ```
///
/// - `ldp x19, x20, [sp], #16`: Load Pair，从栈加载两个寄存器
/// - `#16` 表示后递增 SP（加载后 SP = SP + 16）
/// - 恢复顺序：从 x19 到 x30（正序）
///
/// # 调用约定
///
/// - `extern "C"`: 使用 C ABI，确保与汇编兼容
/// - `unsafe`: 操作裸指针和寄存器，编译器无法保证安全
/// - 参数通过通用寄存器传入，使用命名占位符语法
///
/// # 栈布局示例
///
/// ```text
/// 当前线程栈（保存后）：
/// +----------------+
/// | x29 (FP)       | ← SP（保存后）
/// +----------------+
/// | x30 (LR)       |
/// +----------------+
/// | x27-x28        |
/// +----------------+
/// | x25-x26        |
/// +----------------+
/// | x23-x24        |
/// +----------------+
/// | x21-x22        |
/// +----------------+
/// | x19-x20        | ← SP（保存前）
/// +----------------+
/// ```
///
/// 目标线程栈（恢复前）：
/// ```text
/// +----------------+
/// | x29 (FP)       | ← SP（切换后）
/// +----------------+
/// | x30 (LR)       |
/// +----------------+
/// | ... 其他寄存器  |
/// +----------------+
/// | x19-x20        | ← 目标线程之前保存的状态
/// +----------------+
/// ```
///
/// # 注意事项
///
/// - **对称性**：压栈顺序必须与出栈顺序相反
///   - 压栈：x30 → x19（倒序）
///   - 出栈：x19 → x30（正序）
///
/// - **栈平衡**：压栈 6×16=96 字节，出栈 6×16=96 字节
///   - 切换后 SP 指向目标线程栈顶
///
/// - **LR 恢复**：恢复 x30 后，函数返回会跳转到目标线程的返回地址
///   - 实现了线程执行的"跳跃"
///
/// # options(nostack)
///
/// 使用 `nostack` 是因为：
/// - 汇编代码手动管理栈（压栈/出栈）
/// - 编译器不需要额外的栈检查
/// - 避免栈操作的冲突和性能开销
///
/// # 安全性
///
/// **调用者必须确保：**
/// - `_from` 和 `_to` 指针有效
/// - `_to->sp` 指向有效的栈空间
/// - 目标线程栈中保存的寄存器状态正确
///
/// 否则会导致：
/// - 内存访问错误
/// - 栈崩溃
/// - 未定义行为
pub unsafe extern "C" fn hw_context_switch(_from: *mut ThreadContext,
                _to: *const ThreadContext) {
    asm!(
        "stp x29, x30, [sp, #-16]!",
        "stp x27, x28, [sp, #-16]!",
        "stp x25, x26, [sp, #-16]!",
        "stp x23, x24, [sp, #-16]!",
        "stp x21, x22, [sp, #-16]!",
        "stp x19, x20, [sp, #-16]!",
        "mov x0, sp",
        "str x0, [{from_reg}]",
        "ldr x0, [{to_reg}]",
        "mov sp, x0",
        "ldp x19, x20, [sp], #16",
        "ldp x21, x22, [sp], #16",
        "ldp x23, x24, [sp], #16",
        "ldp x25, x26, [sp], #16",
        "ldp x27, x28, [sp], #16",
        "ldp x29, x30, [sp], #16",
        from_reg = in(reg) _from,
        to_reg = in(reg) _to,
        options(nostack)
    );
}

